function loadPdfAsBase64(filename: string): string {
  const pdfPath = resolve(SUPABASE_TEST_DATA_DIR, filename);
  const buffer = readFileSync(pdfPath);
  return buffer.toString("base64");
}

export interface SupabaseSwiggyTestCase {
  parsedEmailId: string;
  emailData: EmailData;
  expected: SwiggyExpectedOutput;
  storageUrl: string;
}

